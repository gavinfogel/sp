	<p>
		[the Sea Peoples were] a shadowy multitude of many different tribes and cultures who <span
			class="text-purple-300">roved violently</span
		> across the near east upon a mighty fleet of bird-prowed ships.
	</p>
	<div class="text-base mt-12">Gordon Doherty, Death from the Waves - the Sea Peoples</div>
</div>

<div class="mt-12 text-xl">
	Although Gordon Doherty goes on to clarify that this is not a likely interpretation of the Sea
	Peoples, this kind of sensationalism is still present in some narratives, though more subtly put.
	Additionally, because of the engagmenet that sensational narratives can bring, it is often that
	public sources will lean into this idea at first, which can lead to a misunderstanding of the Sea
	Peoples. However, despite wanting to believe that the Sea Peoples were a peaceful group who were
	simply victims of circumstance, the evidence is clear that the Sea Peoples were indeed
	militarized, organized, and had a significant impact on the Bronze Age civilizations.
</div>

<p class="text-xl mt-12">
	Some scholars argue that the groups must have formed a somewhat unified military confederation;
	the textual sources from Medinet Habu (the location of Ramesses III's mortuary temple) do refer to
	the Sea People's as having 'leaders' or 'great ones', and scholars commonly attribute the two
	battles depicted at Medinet Habu as indicative of the Sea People's unified leadership; that is,
	without any sort of unified leadership, the level of military might necessary to even end up on
	the Egyptian coast would have been impossible to achieve. However, the extent to which the Sea
	Peoples were organized is still a topic of debate, and it is likely that the Sea Peoples were made
	up of a series of different tribes, each with their own distinct pieces of culture.
</p>

<p class="text-xl mt-12">
	Once piece of evidence that supports the idea of a unified military confederation is the fact that
	in some of the reliefs at Medinet Habu, the Sea Peoples, while captured, are depicted as having
	more decorated handcuffs in more comfortable positions. This is likely indicative of the fact that
	Some Sea Peoples were more important than others, and that there was a hierarchy within the Sea
	Peoples. Similarly, because multiple leaders of tribes are mentioned, it is likely the overall
	confederation still remained somewhat decentralized. However, this is not conclusive evidence, and
	it is still a topic of debate.
</p>

<p class="text-xl mt-12">
	Were these military attacks responsible for the downfall of the Bronze Age? This is yet another
	empty question, with no conclusive answer. Certainly the Sea Peoples destroyed many cities, but it
	is not clear if the military action was the cause or a sympton of the collapse of the bronze age.
	As Eric Cline writes in his book 1177 B.C., similar to how critical infrastructure faults today
	could lead to massive societal changes and potentially warfare, the same could have been true for
	the Bronze Age. Many factors of the downfall of the Bronze Age may have been self inforcing
	positive feedback loops that perpetuated each other in the collapse of the Bronze Age. Are the Sea
	Peoples scapegoated too much? It is unlikely we can make a conclusive answer about this question.
</p>

